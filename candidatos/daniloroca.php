                <p class="text-muted" style="text-align: justify;">
                    Nace en la ciudad de Guatemala en 1945, hijo del abogado Raúl Roca y la maestra Sara Barillas. Nació en un entorno demarcado por profundos cambios de la realidad política, económica y social de Guatemala, producto todo esto de la gesta revolucionaria del 20 de octubre de 1944, más conocida como la Revolución de Octubre.
                </p>
                <p class="text-muted" style="text-align: justify;">
                    Sobrino segundo del Doctor Juan José Arévalo Bermejo, desde muy niño fue testigo del desarrollo los procesos que se gestaron en ese tiempo y el derivado del movimiento contrario que se desarrolló a partir de 1954.
                </p>
                <p class="text-muted" style="text-align: justify;">
                    En medio de una dinámica socio-política intensa, desde la niñez se vio vinculado en acciones con grupos infantiles que despuntaban con el entusiasmo de una nueva era. Con una educación derivada de sus convicciones que asentaba la confianza en la enseñanza pública, estudió en la Escuela República de Costa Rica su primaria y fue un dirigente reconocido en el Instituto Nacional Central para Varones en la secundaria, del cual fue su presidente estudiantil. En esa época se registran sus primeros grandes triunfos como orador, al haber ganado todos los concursos de oratoria en los que participó frente a institutos públicos y privados.
                </p>
                <p class="text-muted" style="text-align: justify;">
                    Su formación universitaria la realizó en la Universidad de San Carlos de Guatemala, de la cual egresa con el grado académico de Licenciado en Ciencias Jurídicas y Sociales y las carreras profesionales de Abogado y Notario. Egresado de la Escuela Interamericana de Educación Democrática con sede en Costa Rica, de cuya promoción fue el mejor alumno de toda América Latina, le valió para hacer su tesis en Puerto Rico sobre la Naturaleza Jurídica del Estado Libre Asociado de Puerto Rico. Vinculado con ese tipo de actividades a los 20 años es nombrado Sub-Director General de Desarrollo de la Comunidad de la Presidencia de la República durante el Gobierno del Presidente Julio Cesar Méndez Montenegro. Estudió también en El Centro Interamericano del Instituto de Relaciones Humanas de la Universidad de Loyola en Nueva Orleans, en el cual cursó junto con el Ex Presidente Vinicio Cerezo.
                </p>
                <p class="text-muted" style="text-align: justify;">
                    Consultor de la International Development Foundation a principios de los años 1970’s para programas de desarrollo comunitario; asesoró también al Presidente Joaquín Balaguer en República Dominicana y al Presidente José Figueres en Costa Rica (1969-1970).
                </p>
                <p class="text-muted" style="text-align: justify;">
                    Fue Director General de Transportes (1974-1977), Consultor de entidades privadas, columnista de la Revista Domingo por 5 años con los pseudónimos Edmundo Deantés y Julián Parrillas, siendo de los pocos que han logrado escribir 3 páginas simultáneamente en una sola edición (1986-1991). Se desempeñó como Abogado de la Cámara Guatemalteca de Periodismo (1990). Diputado al Parlacen y ponente del Tratado de la Nacionalidad Centroamericana; se le reconoce como el Primer Presidente Electo Democráticamente en un proceso en el Parlamento Centroamericano, cargo que declinó en aras de la instalación del Organísmo. Candidato Presidencial por la Unión Centro Nacional, fundada por Jorge Carpio Nicolle (1999).
                </p>
                <p class="text-muted" style="text-align: justify;">
                    Abogado litigante en los últimos 25 años, ha participado como conferencista nacional e internacional en distintos foros relacionados con la temática jurídica, siendo su más reciente aporte su participación en el Foro del Fortalecimiento del Sector Justicia en América Latina, en Montevideo Uruguay, donde disertó sobre el tema “La Politización de la Justicia y la Judicialización de la Política”. Entre sus condecoraciones más apreciadas se encuentra la Orden Francisco Morazán, la más alta condecoración que otorga el Parlamento Centroamericano en toda la región.
